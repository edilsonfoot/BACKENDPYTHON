'''
Crie uma lista valores = [10, 20, 30, 40]. Use um laço for para calcular manualmente a soma de todos os valores.
'''

#lista principal
valores = [10, 20, 30, 40]

#variável
soma = 0

#calculando manualmaente e exibindo a soma total dos valores da lista
for v in valores:
    soma += v
print(soma)