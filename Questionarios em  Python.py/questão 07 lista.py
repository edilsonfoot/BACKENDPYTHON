'''
Percorra a lista livros e exiba cada livro com a frase: "O livro <nome do livro> é interessante"
'''

livros = ["romance", "religião", "Duna", "politica", "sociologia", "matematica"]

#Usando a estrutura de repetição "for" para percorrer a lista

for livro in livros:
        print(f"O livro '{livro}' é interessante!")